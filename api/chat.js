module.exports = async function handler(req, res) {
  if (req.method !== 'POST') return res.status(405).json({error:'Разрешён только POST-запрос.'});
  const message = typeof req.body?.message === 'string' ? req.body.message.trim() : '';
  if (!message || message.length > 1500) return res.status(400).json({error:'Напиши вопрос длиной до 1500 знаков.'});
  const apiKey = process.env.GEMINI_API_KEY;
  if (!apiKey) return res.status(503).json({error:'Серверный ключ не задан. Используй клиентский чат (настрой ключ через интерфейс) или запусти через «Запустить Ирку-всезнайку.cmd».'});
  const model = process.env.GEMINI_MODEL || 'gemini-3.5-flash-lite';
  const context = COURSE_CONTEXT[req.body?.courseId] || 'Общий контекст: бытовая техника и безопасный подбор запчастей.';
  try {
    const response = await fetch(`https://generativelanguage.googleapis.com/v1beta/models/${encodeURIComponent(model)}:generateContent`, {
      method:'POST',
      headers:{'Content-Type':'application/json','x-goog-api-key':apiKey},
      body:JSON.stringify({
        systemInstruction:{parts:[{text:`${SYSTEM_PROMPT}\n\nКонтекст выбранного раздела: ${context}`}]},
        contents:[{role:'user',parts:[{text:message}]}],
        generationConfig:{temperature:0.35,maxOutputTokens:900}
      })
    });
    const data = await response.json();
    if (!response.ok) throw new Error(data?.error?.message || 'Ошибка Gemini API');
    const answer = data?.candidates?.[0]?.content?.parts?.map(p=>p.text||'').join('').trim();
    if (!answer) throw new Error('Помощник вернул пустой ответ.');
    return res.status(200).json({answer});
  } catch (error) {
    console.error('Gemini request failed:', error.message);
    return res.status(502).json({error:'Миса временно не может ответить. Попробуй ещё раз чуть позже.'});
  }
};
